from django.db import models
from sub_categriy.models import SubCategoryModel
from user.models import UserModel


# Create your models here.


class ProductModel(models.Model):
    author = models.ForeignKey(UserModel, on_delete=models.CASCADE, related_name='products')
    name = models.CharField(max_length=100)
    description = models.TextField()
    price = models.DecimalField(max_digits=10, decimal_places=2)
    stock = models.PositiveIntegerField(default=0)
    image = models.ImageField(upload_to="products/", blank=True, null=True)
    sub_category = models.ForeignKey(SubCategoryModel, on_delete=models.CASCADE, related_name='products')
    created_at = models.DateTimeField(auto_now_add=True)
    updated_at = models.DateTimeField(auto_now=True)

    def __str__(self):
        return self.name